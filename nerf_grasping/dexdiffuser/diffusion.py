    def sample(self):
        self.model.eval()

        config = self.config
        with torch.no_grad():
            n = config.sampling.batch_size
            x = torch.randn(
                n,
                GRASP_DIM,
                device=self.device,
            )
            cond = torch.randn(
                n,
                N_PTS,
                device=self.device,
            )

            x = self.sample_image(x, cond, self.model)
            return x

    def sample_image(
        self,
        x,
        cond,
        model,
        last=True,
        sample_type="generalized",
        skip_type="uniform",
        skip=1,
        timesteps=1000,
        eta=0.0,
    ):
        if skip_type == "uniform":
            skip = self.num_timesteps // timesteps
            seq = range(0, self.num_timesteps, skip)
        elif skip_type == "quad":
            seq = np.linspace(0, np.sqrt(self.num_timesteps * 0.8), timesteps) ** 2
            seq = [int(s) for s in list(seq)]
        else:
            raise NotImplementedError

        if sample_type == "generalized":
            xs = generalized_steps(x, cond, seq, model, self.betas, eta=eta)
            x = xs
        elif sample_type == "ddpm_noisy":
            x = ddpm_steps(x, cond, seq, model, self.betas)
        else:
            raise NotImplementedError
        if last:
            x = x[0][-1]
        return x


